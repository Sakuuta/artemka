from turtle import*
coutn = int(input("Сколько фигур нарисовать? "))
penup()
goto(-330,0)
pendown()
for i in range(coutn):
    fd(50)
    lt(90)
    fd(30)
    lt(90)
    fd(40)
    rt(90)
    fd(30)
    rt(90)
    fd(90)
    rt(90)
    fd(30)
    rt(90)
    fd(40)
    lt(90)
    fd(30)
    lt(90)
    fd(50)
exitonclick()